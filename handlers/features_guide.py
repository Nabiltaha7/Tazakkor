def handle_features_command(message) -> bool:
    text = (message.text or "").strip()
    if text not in ("مميزات البوت", "مساعدة", "مميزات"):
        return False

    uid = message.from_user.id
    cid = message.chat.id
    _send_main_menu(cid, uid, reply_to=message.message_id)
    return True


def _send_main_menu(cid: int, uid: int, reply_to: int = None, call=None) -> None:
    owner = (uid, cid)
    text  = (
        f"✨ <b>دليل تَذَكُّر | 𝐓𝐚𝐳𝐚𝐤𝐤𝐨𝐫</b>\n"
        f"{get_lines()}\n\n"
        "اختر قسماً لتعرف كيف يعمل البوت:"
    )

    buttons = [
        btn(f"{v['emoji']} {v['title']}", "feat_section",
            {"sec": k, "p": 0}, owner=owner, color=_B)
        for k, v in _SECTIONS.items()
    ]
    n = len(buttons)
    buttons.append(btn("❌ إغلاق", "feat_close", {}, owner=owner, color=_RD))

    # 2 per row for sections, 1 for close
    layout = [2] * (n // 2) + ([1] if n % 2 else []) + [1]

    if call:
        edit_ui(call, text=text, buttons=buttons, layout=layout)
    else:
        send_ui(cid, text=text, buttons=buttons, layout=layout,
                owner_id=uid, reply_to=reply_to)


def _page_buttons(action: str, extra: dict, total: int, current: int,
                  owner: tuple) -> tuple[list, list]:
    """Numbered page buttons — current page highlighted in green, 4 per row."""
    page_btns = [
        btn(str(i + 1), action, {**extra, "p": i},
            owner=owner, color=_GR if i == current else _B)
        for i in range(total)
    ]
    rows   = [page_btns[i:i + 4] for i in range(0, len(page_btns), 4)]
    layout = [len(r) for r in rows]
    return [b for r in rows for b in r], layout


